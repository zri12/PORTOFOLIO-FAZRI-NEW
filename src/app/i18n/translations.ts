const autoPhraseIdToEn = new Map(autoPhraseEntries.map(([en, id]) => [id, en]));

function preserveCase(source: string, translated: string) {
  if (source.toUpperCase() === source) return translated.toUpperCase();
  if (source[0]?.toUpperCase() === source[0]) return `${translated[0]?.toUpperCase() || ""}${translated.slice(1)}`;
  return translated;
}

function autoTranslateByPhrases(value: string, language: Language) {
  if (value.length < 24) return value;
  const wordCount = value.trim().split(/\s+/).filter(Boolean).length;
  if (wordCount > 5) return value;
  const entries = language === "id" ? autoPhraseEntries : Array.from(autoPhraseIdToEn.entries());
  let changed = false;
  let result = value;

  entries
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([from, to]) => {
      const pattern = new RegExp(`\\b${from.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`, "gi");
      result = result.replace(pattern, (match) => {
        changed = true;
        return preserveCase(match, to);
      });
    });

  return changed ? result : value;
}

export function getInitialLanguage(): Language {
  if (typeof window === "undefined") return "en";
  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (saved === "en" || saved === "id") return saved;
  return "en";
}

export function translateText(value: string, language: Language) {
  if (language === "en") return idToEn.get(value) || autoTranslateByPhrases(value, language);
  return enToId.get(value) || autoTranslateByPhrases(value, language);
}
